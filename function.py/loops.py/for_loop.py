#for loop ka use list, tuple, string, range, dictionary, set ke elements par iterate karne ke liye hota hai.
#for loop ka syntax:
#for variable in sequence:
#    # code to be executed
#Example 1: List ke elements par iterate karna
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
#Output: apple
#        banana
#        cherry

#Example 2: String ke characters par iterate karna
name = "John"
for char in name:
    print(char)
#Output: J
#        o
#        h
#        n
#Example 3: Range ke numbers par iterate karna
for i in range(5):
    print(i)